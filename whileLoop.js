// print 1 to 100 using ehile loop.
let i = 1
while (i <= 100) {
    console.log(i)
    i++
}


// print even number using while loop.
let j = 2
while (j <= 100) {
    console.log(j)
    j = j + 2
}

// print 100 to 1 using ehile loop.
let k = 100
while (k >= 1) {
    console.log(k)
    k--
}


// print odd number using while loop.
let L = 1
while (L <= 100) {
    console.log(L)
    L = L + 2
}